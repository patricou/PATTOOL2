def build_from_en(en: dict[str, str], transform) -> dict[str, str]:
    return {k: transform(v) for k, v in en.items()}


def build_from_fr(fr: dict[str, str], transform) -> dict[str, str]:
    return {k: transform(v) for k, v in fr.items()}


def merge_preferred(base: dict[str, str], existing: dict[str, str] | None, en: dict[str, str]) -> dict[str, str]:
    out: dict[str, str] = {}
    for k, en_val in en.items():
        if existing and k in existing and existing[k] != en_val:
            out[k] = existing[k]
        elif k in base:
            out[k] = base[k]
        else:
            out[k] = en_val
    return out


def main() -> None:
    en = load_json(LOCALES / "en.json")
    fr = load_json(LOCALES / "fr.json")
    en_keys = list(en.keys())
    assert len(en_keys) == 280, len(en_keys)

    # Generated locale packs
    generated = {
        "de": build_from_fr(fr, fr_to_de),
        "es": build_from_fr(fr, fr_to_es),
        "it": build_from_fr(fr, fr_to_it),
        "ru": finalize_with_fr_fallback(build_from_en(en, en_to_ru), en, fr, fr_to_ru),
        "ar": finalize_with_fr_fallback(build_from_en(en, en_to_ar), en, fr, fr_to_ar),
        "he": finalize_with_fr_fallback(build_from_en(en, en_to_he), en, fr, fr_to_he),
        "cn": finalize_with_fr_fallback(build_from_en(en, en_to_cn), en, fr, fr_to_cn),
        "jp": finalize_with_fr_fallback(build_from_en(en, en_to_jp), en, fr, fr_to_jp),
        "el": finalize_with_fr_fallback(build_from_en(en, en_to_el), en, fr, fr_to_el),
        "in": finalize_with_fr_fallback(build_from_en(en, en_to_in), en, fr, fr_to_in),
    }

    locale_map: dict[str, dict[str, str]] = {
        "en": en,
        "fr": fr,
        **generated,
    }

    files = sorted(I18N.glob("*.json"))
    for path in files:
        if fix_json_text(path):
            print(f"fixed JSON comma: {path.name}")
        try:
            data = load_json(path)
        except json.JSONDecodeError as exc:
            print(f"ERROR cannot load {path.name}: {exc}")
            continue
        code = path.stem
        if code not in locale_map:
            print(f"skip {path.name}: no locale pack")
            continue
        existing = data.get("METEO_FRANCE")
        merged = merge_preferred(locale_map[code], existing, en)
        if list(merged.keys()) != en_keys:
            missing = set(en_keys) - set(merged.keys())
            raise SystemExit(f"{path.name} missing keys after merge: {missing}")
        data["METEO_FRANCE"] = merged
        save_json(path, data)
        print(f"updated {path.name}: {len(merged)} keys")

    # Write generated packs for review
    for code, pack in generated.items():
        save_json(LOCALES / f"{code}.json", pack)


if __name__ == "__main__":
    main()
