def finalize_with_fr_fallback(pack: dict[str, str], en: dict[str, str], fr: dict[str, str], fr_transform) -> dict[str, str]:
    out = dict(pack)
    for k, en_val in en.items():
        if out.get(k) == en_val and k in fr:
            out[k] = fr_transform(fr[k])
    return out


