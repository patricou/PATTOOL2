# Fix known JSON syntax errors (missing comma after ERR_EXPORT)
JSON_COMMA_FIXES = [
    (r'("ERR_EXPORT": "[^"]*")\s*\n(\s*"ERR_SHARE_WHATSAPP")', r"\1,\n\2"),
    (r',(\s*\n\s*\})', r"\1"),
]


def load_json(path: Path) -> dict:
    raw = path.read_text(encoding="utf-8-sig")
    return json.loads(raw)


def save_json(path: Path, data: dict) -> None:
    path.write_text(json.dumps(data, ensure_ascii=False, indent=4) + "\n", encoding="utf-8")


def fix_json_text(path: Path) -> bool:
    text = path.read_text(encoding="utf-8-sig")
    original = text
    for pattern, repl in JSON_COMMA_FIXES:
        text = re.sub(pattern, repl, text)
    if text != original:
        path.write_text(text, encoding="utf-8")
        return True
    return False


def apply_repl(text: str, repl: list[tuple[str, str]]) -> str:
    out = text
    for a, b in repl:
        out = out.replace(a, b)
    return out


